function gc = gradc(x)
n = length(x);
gc1 = -[-4,-5,0,0,0,0,3,-9,0,0];
gc2 = -[-6*x(1)+12,-8*x(2)+24,-4*x(3),7,0,0,0,0,0,0];
gc3 = -[-10,8,0,0,0,0,17,-2,0,0];
gc4 = -[-2*x(1)+2*x(2),-4*(x(2)-2)+2*x(1),0,0,-14,6,0,0,0,0];
gc5 = -[8,-2,0,0,0,0,0,0,-5,2];
gc6 = -[-10*x(1),-8,-2*(x(3)-6),2,0,0,0,0,0,0];
gc7 = -[3,-6,0,0,0,0,0,0,-24*(x(9)-8),7];
gc8 = -[-(x(1)-8),-4*(x(2)-4),0,0,-6*x(5),1,0,0,0,0];
Dc = [gc1;gc2;gc3;gc4;gc5;gc6;gc7;gc8;-eye(n);eye(n)];
gc = Dc';
